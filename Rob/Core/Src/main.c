void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim)
{
	static float maxval = 0;
	static int32_t minval = 0;
	static uint32_t maxcnt = 0;
	static uint32_t mincnt = 0;
	static uint32_t timcnt = 0;
	static uint32_t halfperiod = 0;
	//static uint32_t numpeaks = 0;
	static float maxave = 0;
	float dataval = 0;

	//uint16_t start =__HAL_TIM_GET_COUNTER(&htim16); used to determine how much time is used in this ISR

	static bool is_initialized = false;
	if(is_initialized == false)
	{
		//__HAL_TIM_SET_COUNTER(&htim16,0);  // set the counter value a 0
		TIM16->CNT = 0;
		is_initialized = true;
	}

	else
	{
		//__HAL_TIM_GET _COUNTER(&htim16) < us)
		dataval = HAL_ADC_GetValue(&hadc1);
		//dataval = get_noised_sine_data(__HAL_TIM_GET_COUNTER(&htim16));
		//dataval = get_noised_sine_data(__HAL_TIM_GET_COUNTER(&htim16));
		//dataval = get_noised_sine_data(TIM16->CNT);
		if((dataval > maxval)&&(maxcnt<DBNCE_WDTH)) //value is still rising
		{
			maxcnt=0;
			maxval = dataval;
		}
		if((dataval < minval)&&(mincnt<DBNCE_WDTH)) //value is falling
		{
			mincnt = 0;
			minval = dataval;
		}
		if((maxcnt>DBNCE_WDTH)&&(mincnt>DBNCE_WDTH)) //peak has been passed
		{
			NUMPEAKS++;
			//halfperiod = NUMPEAKS>1?timcnt:0;  //remove first instance of "peak" because the 1st peak likely happens in the middle of a wave
			if(NUMPEAKS>1)
			{
				halfperiod = timcnt;
				maxave = 0;
			}
			if(maxcnt>mincnt) //means we're at a low peak
			{
				maxval = minval; //we can start comparing the maxval starting from lowest point as the value increases
				maxcnt = 0;
			}
			else //means we're at a high peak
			{
				minval = maxval; //we can start comparing the minval starting from highest point as the value decreases
				mincnt = 0;
			}
			AVE_HALFPEAK_TIME += halfperiod;
			maxave += maxave;
			timcnt = 0;
			//__HAL_TIM_SET_COUNTER(&htim16,0);  // set the counter value a 0
			TIM16->CNT = 0;  // set the counter value a 0
		}
		mincnt++;
		maxcnt++;
		timcnt= TIM16->CNT;//__HAL_TIM_GET_COUNTER(&htim16);
		TIM16->CNT = 0;//__HAL_TIM_SET_COUNTER(&htim16, 0);

	//	AVE_HALFPEAK_TIME = AVE_HALFPEAK_TIME/(NUMPEAKS-1); //remove the first peak because we might not be starting from 0

		//uint16_t finish = __HAL_TIM_GET_COUNTER(&htim16);
		//volatile uint16_t diff = finish-start;
		num_calls++;
	}

	return;
}

// Function to change the duty cycle
void Set_Duty_Cycle(float percentage)
{
    // Ensure the percentage is within the valid range
	percentage = ((percentage >= 0)&&(percentage<=100))?percentage:0;

    // Calculate the compare value based on the duty cycle
    uint32_t duty_cycle = (uint32_t)((percentage / 100.0f) * (TIM1->ARR + 1));
    TIM1->CCR1 = duty_cycle; // Set the compare value

    // In PWM mode 2, the output is high when the counter is greater than the CCR1 value.
    // This means the output will turn on at the end of the cycle.
}


